# Define function to get top-level comments from a YouTube video
def get_comments_with_id(video_id, api_key, max_comments=2000):
    youtube = build("youtube", "v3", developerKey=api_key)

    comments = []
    next_page_token = None

    # Request up to 100 comment per page, ordered by relevance
    while len(comments) < max_comments:
        request = youtube.commentThreads().list(
            part="snippet",
            videoId=video_id,
            maxResults=100,
            order="relevance",
            pageToken=next_page_token
        )
        response = request.execute()

        for item in response["items"]:
            snippet = item["snippet"]["topLevelComment"]["snippet"]
            comments.append({
                "comment_id": item["snippet"]["topLevelComment"]["id"],
                "text": snippet["textDisplay"],
                "author": snippet["authorDisplayName"],
                "likes": snippet["likeCount"],
                "published_at": snippet["publishedAt"],
                "type": "top_level"
            })

        # exit loop if no further pages
        next_page_token = response.get("nextPageToken")

        if not next_page_token:
            break

        # Pause between requests to avoid hitting API rate limits
        time.sleep(0.5)
        print(f"has captured {len(comments)} comments...")

    return comments


top_comments = get_comments_with_id(VIDEO_ID, API_KEY)
