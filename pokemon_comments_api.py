# Define function to get replies for a given top-level comment using parent comment ID
def get_replies(api_key, parent_id):
    youtube = build("youtube", "v3", developerKey=api_key)
    replies = []

    try:
        request = youtube.comments().list(
            part="snippet",
            parentId=parent_id,
            maxResults=100
        )
        response = request.execute()

        # Extract relevant fields from each reply
        for item in response["items"]:
            snippet = item["snippet"]
            replies.append({
                "comment_id": item["id"],
                "text": snippet["textDisplay"],
                "author": snippet["authorDisplayName"],
                "likes": snippet["likeCount"],
                "published_at": snippet["publishedAt"],
                "type": "reply"
            })
    except Exception as e:
        print(f"Error: {e}")

    return replies

